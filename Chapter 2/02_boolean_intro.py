# Boolean(bool) is a basic data type in python that represents either True or False values.
# It is often used in conditional statements, loops and logical operations.
# Boolean(bool) values are case-sensitive, meaning that True and False are must be capialized there first letter.

my_true = True
my_false = False

# Boolean values can be used in conditional statements to control the flow of the program.
if (my_true):
    print(f"This is not {my_false}") # Output: "This is not False"
